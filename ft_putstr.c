#include "ft_printf.h"

int	ft_putstr(char	*str)
{
	int	i;

	i = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		i = 6;
	}
	while (str && str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
